//Javascript Events

/* HTML Events

An html event can be something the browser does or something a user does
JS lets you execute code when events are detected

<element event='some JavaScript/function call'>

Common HTML Events
    onchange - html element is changed
    onclick - user clicks an html element
    onmouseover - user moves the mouse over an HTML element
    onmouseout - user moves the mouse away from an HTML element
    onkeydown - user pushes a keyboard key
    onload - browser finishs loading the page
*/

